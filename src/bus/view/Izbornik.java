    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed

        Query query = HibernateUtil.getSession().createQuery(
                "select count(*) from Autobus where obrisano=false");
        Long count = (Long) query.uniqueResult();

        if (count.equals(new Long(0))) {
            JOptionPane.showMessageDialog(getRootPane(), "Ne postoje autobusi!");
            new Autobusi().setVisible(true);
            return;
        }

        query = HibernateUtil.getSession().createQuery(
                "select count(*) from Lokacija where obrisano=false");
        count = (Long) query.uniqueResult();

        if (count.equals(new Long(0))) {
            JOptionPane.showMessageDialog(getRootPane(), "Ne postoje lokacije!");
            new Lokacije().setVisible(true);
            return;
        }

        new Linije().setVisible(true);
    }//GEN-LAST:event_jMenuItem2ActionPerformed

