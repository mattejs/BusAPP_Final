    private void listaRValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaRValueChanged
        if (evt.getValueIsAdjusting()) {
            return;
        }
        Rezervacija r = listaR.getSelectedValue();

        if (r == null) {
            String t = "-";
            lblIme.setText(t);
            lblPrezime.setText(t);
            lblOIB.setText(t);
            lblKontakt.setText(t);
            lblBrojSjedala.setText(t);
            return;
        }

        lblIme.setText(r.getIme());
        lblPrezime.setText(r.getPrezime());
        lblOIB.setText(r.getOIB());
        lblKontakt.setText(r.getKontakt());
        lblBrojSjedala.setText(String.valueOf(r.getSjedalo()));
    }//GEN-LAST:event_listaRValueChanged

    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged

        if (evt.getValueIsAdjusting()) {
            return;
        }

        Linija l = lista.getSelectedValue();

        if (l == null) {
            return;
        }

        lblPolaziste.setText(l.getPolaziste().toString());
        lblOdrediste.setText(l.getOdrediste().toString());
        lblPolazak.setText(l.getPolazak().toString());
        lblDolazak.setText(l.getDolazak().toString());
        lblAutobus.setText(l.getAutobus().toString());
        lblCijena.setText(l.getCijena().toString());
        lblUdaljenost.setText(l.getUdaljenost().toString());

        ucitajRezervacije();
        
    }//GEN-LAST:event_listaValueChanged

