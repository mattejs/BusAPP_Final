    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        
        if(!kontrola()){
            return;
        }
        
        Lokacija l = new Lokacija();
        l = napuniObjekt(l);
        obrada.save(l);
        ucitajPodatke();
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnPromjenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjenaActionPerformed
        Lokacija l = lista.getSelectedValue();
        
        if (l == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite lokaciju");
            return;
        }

        if (!kontrola()) {
            return;
        }
        l = napuniObjekt(l);
        obrada.save(l);
        ucitajPodatke();


    }//GEN-LAST:event_btnPromjenaActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        Lokacija l = lista.getSelectedValue();
        if (l == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite lokaciju");
            return;
        }

        boolean mozeObrisati = true;

        for (Linija l1 : l.getPolaziste()) {
            if (!l.isObrisano()) {
                mozeObrisati = false;
                break;
            }
        }

        for (Linija l1 : l.getOdrediste()) {
            if (!l.isObrisano()) {
                mozeObrisati = false;
                break;
            }
        }

        if (!mozeObrisati) {
            JOptionPane.showMessageDialog(getParent(), "Lokacija se ne može obrisati jer je polaziše ili odredište na nekoj od linija");
            return;
        }

        obrada.delete(l);
        ucitajPodatke();
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged
        if (evt.getValueIsAdjusting()) {
            return;
        }

        Lokacija l = lista.getSelectedValue();

        if (l == null) {
            return;
        }

        txtNaziv.setText(l.getNaziv());
    }//GEN-LAST:event_listaValueChanged

    private boolean kontrola() {
            

        if (txtNaziv.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(getRootPane(), "Obavezan naziv");
            return false;
        }

        return true;
    }


