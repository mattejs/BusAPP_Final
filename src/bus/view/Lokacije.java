    private Lokacija napuniObjekt(Lokacija l) {
        l.setNaziv(txtNaziv.getText());
        return l;
    }

    private void ucitajPodatke() {
        DefaultListModel<Lokacija> model = new DefaultListModel<>();

        List<Lokacija> lista = HibernateUtil.getSession().createQuery(
                "from Lokacija a where a.obrisano=false").list();

        for (Lokacija l : lista) {
            model.addElement(l);
        }

        this.lista.setModel(model);
    }
